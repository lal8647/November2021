package OOP_Inheritence;

public class Car {

	public void start()
	{
		System.out.println("Car---Start");
	}
	
	
	public void stop() {
		
		System.out.println("car--Stop");
	}
	
	public void restart() {
		System.out.println("car---restart");
	}
	
	public static void sunRoof() {
		System.out.println("Car--sunRoof");
	}
	
	
	
	
	
}



